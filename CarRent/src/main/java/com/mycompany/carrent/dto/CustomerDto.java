package com.mycompany.carrent.dto;

/**
 *
 * @author Uthsara Basnayke
 */
public class CustomerDto {
      private String id;
    private String title;
    private String name;
    private int age;
    
    private String address;
    private String city;
   private String zip;
    private String district;
    private String province;

    public CustomerDto() {
    }

    public CustomerDto(String id, String title, String name, int age, String address, String city, String zip, String district, String province) {
        this.id = id;
        this.title = title;
        this.name = name;
        this.age = age;
        this.address = address;
        this.city = city;
        this.zip = zip;
        this.district = district;
        this.province = province;
    }

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the title
     */
    public String getTitle() {
        return title;
    }

    /**
     * @param title the title to set
     */
    public void setTitle(String title) {
        this.title = title;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the age
     */
    public int getAge() {
        return age;
    }

    /**
     * @param age the age to set
     */
    public void setAge(int age) {
        this.age = age;
    }

    /**
     * @return the address
     */
    public String getAddress() {
        return address;
    }

    /**
     * @param address the address to set
     */
    public void setAddress(String address) {
        this.address = address;
    }

    /**
     * @return the city
     */
    public String getCity() {
        return city;
    }

    /**
     * @param city the city to set
     */
    public void setCity(String city) {
        this.city = city;
    }

    /**
     * @return the zip
     */
    public String getZip() {
        return zip;
    }

    /**
     * @param zip the zip to set
     */
    public void setZip(String zip) {
        this.zip = zip;
    }

    /**
     * @return the district
     */
    public String getDistrict() {
        return district;
    }

    /**
     * @param district the district to set
     */
    public void setDistrict(String district) {
        this.district = district;
    }

    /**
     * @return the province
     */
    public String getProvince() {
        return province;
    }

    /**
     * @param province the province to set
     */
    public void setProvince(String province) {
        this.province = province;
    }

    @Override
    public String toString() {
        return "CustomerDto{" + "id=" + id + ", title=" + title + ", name=" + name + ", age=" + age + ", address=" + address + ", city=" + city + ", zip=" + zip + ", district=" + district + ", province=" + province + '}';
    }
    
}
